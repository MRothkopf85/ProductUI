    for(i = 0; i < items.length; i++) {
        //Create elements for items and pop windows, and html for individual item details
        var prodDiv = document.createElement("div");
        var popDiv = document.createElement("div");
        var divImage = "<img class='img-fluid' src=" + items[i].imageUrl + ">";
        var divName = "<h3>" + items[i].name + "</h3>";
        var divBrand = "<h6>Brand: " + items[i].brand + "</h6>";
        var divShort = "<h6>" + items[i].shortDescription + "</h6></div>";
        var divLong = "<p>" + items[i].description + "</p></div>";
        var divPrice = "<h4>" + items[i].price + "</h4>";
        var divSale = "<h4 id='sale'>SALE!!  " + items[i].salePrice + "</h4>";
        var rating =  "<p> RATING: " + items[i].rating + " <em>(" + items[i].ratingCount + ")</em></p>";
        var prodId = "<p> Product ID: " + items[i].prodId + "</p>";
        var dtlButton = "<button class='open'>Details</button>";
        var itemId = "itemId-" + i;


        var popUpWindow = "<div class='popup-content'>" + divName + divPrice + divBrand + rating
         + prodId + divLong + "</p><button class='close'>Close</button></div>";

        var blob = divImage + divName + divPrice + divBrand + divShort;


        //Account for null values
        if(items[i].brand == null) {
            if(items[i].salePrice == null) {
                var blob = divImage + divName + divPrice + divShort + dtlButton;
            } else {
                var blob = divImage + divName + divSale + divShort + dtlButton;
            }
        } else {
            if(items[i].salePrice == null) {
                var blob = divImage + divName + divPrice + divBrand + divShort + dtlButton;
            } else {
            var blob = divImage + divName + divSale + divBrand + divShort + dtlButton;
        }

        }

        //Add item info to HTML element
        prodDiv.innerHTML = blob;

        popDiv.innerHTML = popUpWindow;

        //Add class attributes
        prodDiv.setAttribute("class", "col-sm-4 col-lg-2 prodContent");
        popDiv.setAttribute("class", "popup-overlay");

        //Add new element to div
        var currentDiv = document.getElementById("allDiv");
        currentDiv.appendChild(prodDiv);
        prodDiv.appendChild(popDiv);

    };

//appends an "active" class to .popup and .popup-content when the "Open" button is clicked
$(".prodContent button").on("click", function(){
    $(this).parent().children().addClass("active");
    $(this).parent().children().children().addClass("active");
});

//removes the "active" class to .popup and .popup-content when the "Close" button is clicked 
$(".prodContent").on("click", ".close", function(){
    $(this).parent().removeClass("active");
    $(this).siblings().removeClass("active");
});
